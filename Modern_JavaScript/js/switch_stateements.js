// switch statements

const grade = 'D';

switch(grade){
    case 'A':
        console.log('you got a A!');
        break;
        case 'B':
        console.log('you got a B!');
        case 'C':
            
        console.log('you got a C!');
        break;
        case 'D':
           
        console.log('you got a D!');
        break;
        case 'E':
        console.log('you got a E!');
        break;
        default:
            console.log('not a valid grade');
}
